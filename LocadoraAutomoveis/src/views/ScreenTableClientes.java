        private void botaoAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdicionarActionPerformed
                String nome = this.campoNome.getText();
                String sobrenome = this.campoSobrenome.getText();
                String cpf = this.campoCpf.getText();
                String rg = this.campoRg.getText();
                String endereco = this.campoEndereco.getText();

                Cliente cliente = new Cliente(nome, sobrenome, rg, modelo.limpaCpf(cpf), endereco);
                ClienteDAO dao = new ClienteDAO();
                try {
                        dao.adiciona(cliente);
                        this.modelo.adicionaCliente(cliente);
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Erro ao tentar acessar o banco de dados. E=" + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }

        }//GEN-LAST:event_botaoAdicionarActionPerformed

